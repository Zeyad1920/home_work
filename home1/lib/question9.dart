/*
This program calculates the area and perimeter of a rectangle and a square.

*/

void main() {
  double hight = 15; //side lenght
  double width = 10; //opposite side length
  double areaOfRectangle = hight * width; // area of rectangle
  double perimeterOfRectangle = 2 * (hight + width); // perimeter of rectangle
  double areaOfSquare = 2 * hight; //area of square
  double perimeterOfSquare = 4 * hight; // perimeter of square
  print('Area of Rectangle: $areaOfRectangle');
  print('Perimeter of Rectangle: $perimeterOfRectangle');
  print('Area of Square: $areaOfSquare');
  print('Perimeter of Square: $perimeterOfSquare');
}

